package com.an.github.utils

import android.app.Activity
import android.content.Intent
import android.net.Uri
import android.support.v4.app.ActivityCompat
import android.support.v4.app.ActivityOptionsCompat
import com.an.github.AppConstants.Companion.INTENT_POST

import com.an.github.data.local.entity.GithubEntity
import com.an.github.ui.activity.GithubDetailActivity


object NavigatorUtils {

    fun redirectToDetailScreen(activity: Activity,
                               githubEntity: GithubEntity,
                               options: ActivityOptionsCompat) {

        val intent = Intent(activity, GithubDetailActivity::class.java)
        intent.putExtra(INTENT_POST, githubEntity)
        ActivityCompat.startActivity(activity, intent, options.toBundle())
    }

    fun openBrowser(activity: Activity,
                    url: String) {
        val i = Intent(Intent.ACTION_VIEW)
        i.data = Uri.parse(url)
        activity.startActivity(i)
    }
}
